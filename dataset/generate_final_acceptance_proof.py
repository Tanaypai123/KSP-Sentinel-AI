import asyncio
from pprint import pprint
from app.database.connection import SessionLocal
from app.ai.pipeline_runner import PipelineRunner
from app.ai.investigation_brief_builder import InvestigationBriefBuilder
from app.ai.response_formatter import ResponseFormatter

async def main():
    proof_lines = []
    proof_lines.append("# Phase 7.4 Acceptance Verification (Final Runtime Proof)")
    proof_lines.append("This document contains the exact runtime artifacts and responses generated by the Phase 7.4 pipeline.\n")
    
    # Run tests on different FIRs
    queries = [
        ("Murder", "Open FIR KSP-000012"),
        ("Theft", "Open FIR KSP-000004"),
        ("Cyber Fraud", "Open FIR KSP-000123"),
        ("Kidnapping", "Open FIR KSP-000346"),
        ("Financial Crime", "Open FIR KSP-000014")
    ]
    
    for crime_type, query_text in queries:
        proof_lines.append(f"\n## =============================================")
        proof_lines.append(f"## RUNTIME PROOF: {crime_type.upper()}")
        proof_lines.append(f"## =============================================\n")
        
        db = SessionLocal()
        try:
            context = PipelineRunner.run(query_text, db)
            
            # 1. Pipeline context
            reasoning = getattr(context, "investigation_reasoning", None)
            
            # Build Brief
            brief = InvestigationBriefBuilder.build(context)
            
            # -------------------------------------------------------------
            # 2. Confidence Calculation
            # -------------------------------------------------------------
            proof_lines.append(f"### Confidence Calculation ({crime_type})")
            if getattr(brief, "confidence_explanation", None):
                c = brief.confidence_explanation
                score = int(c.score * 100)
                for pf in c.positive_factors:
                    proof_lines.append(f"  [+] {pf}")
                for nf in c.negative_factors:
                    proof_lines.append(f"  [-] {nf}")
                proof_lines.append(f"\n  Formula: {c.calculation_summary}")
                proof_lines.append(f"  Final Score: {score}/100")
                proof_lines.append(f"  Confidence: {score}%")
            else:
                proof_lines.append("  Confidence calculation not found.")
                
            proof_lines.append("\n")
                
            # -------------------------------------------------------------
            # 3. Investigation Progress
            # -------------------------------------------------------------
            proof_lines.append(f"### Investigation Progress ({crime_type})")
            if getattr(brief, "investigation_progress", None):
                p = brief.investigation_progress
                completed_count = 0
                for t in p.tasks:
                    proof_lines.append(f"  {t.task.ljust(25)} {t.status}")
                    if t.status == "Completed":
                        completed_count += 1
                proof_lines.append(f"\n  Completed = {completed_count} / {len(p.tasks)}")
                proof_lines.append(f"  Progress = {p.completion_pct}%")
            else:
                proof_lines.append("  Investigation progress not found.")
                
            proof_lines.append("\n")
            
            # -------------------------------------------------------------
            # 4. Evidence Reasoning Graph
            # -------------------------------------------------------------
            proof_lines.append(f"### Evidence Reasoning Graph ({crime_type})")
            if getattr(brief, "correlated_evidence", None):
                for link in brief.correlated_evidence:
                    proof_lines.append(f"  {link}")
            else:
                proof_lines.append("  No correlated evidence found.")
                
            proof_lines.append("\n")
            
            # -------------------------------------------------------------
            # 5. Recommendation Reasoning Chain
            # -------------------------------------------------------------
            proof_lines.append(f"### Recommendation Reasoning Chain ({crime_type})")
            if getattr(brief, "recommendations", None):
                for i, r in enumerate(brief.recommendations, 1):
                    proof_lines.append(f"  Recommendation {i}: {r.text}")
                    proof_lines.append(f"    Evidence        -> {r.evidence}")
                    proof_lines.append(f"    Reasoning       -> {r.reason}")
                    proof_lines.append(f"    Risk            -> {r.risk}")
                    proof_lines.append(f"    Recommendation  -> {r.recommendation}")
                    proof_lines.append(f"    Expected Impact -> {r.expected_impact}")
                    proof_lines.append("")
            else:
                proof_lines.append("  No recommendations generated.")
                
            proof_lines.append("\n")
            
            # -------------------------------------------------------------
            # 6 & 7. Cross-Section Validation & Traceability
            # -------------------------------------------------------------
            proof_lines.append(f"### Traceability ({crime_type})")
            proof_lines.append("  (Ensured by ContextNormalizer reading from Case Master)")
            cases = getattr(context, "normalized_cases", []) or []
            for c in cases:
                proof_lines.append(f"  CaseMasterID -> {c.fir_number}")
                proof_lines.append(f"    Crime Category: {c.classification.crime_category}")
                
            proof_lines.append("\n")
            
            # -------------------------------------------------------------
            # 10. Final Rendered Report
            # -------------------------------------------------------------
            proof_lines.append(f"### Final Investigation Report ({crime_type})")
            rendered = ResponseFormatter._render(brief)
            proof_lines.append("```")
            proof_lines.append(rendered)
            proof_lines.append("```\n")
            
        except Exception as e:
            proof_lines.append(f"ERROR processing {crime_type}: {e}")
        finally:
            db.close()

    with open("/Users/tanaysharma/.gemini/antigravity-ide/brain/7187975c-2840-42e6-a763-e312dd65c94b/phase_7_4_acceptance_proof.md", "w") as f:
        f.write("\n".join(proof_lines))
    print("Proof generated to artifact!")

asyncio.run(main())
